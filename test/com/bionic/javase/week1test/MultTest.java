package com.bionic.javase.week1test;

import com.bionic.javase.week1.Mult;

public class MultTest {
	
	private Mult c = new Mult();
	
	@Test
	public void testMultWithZero() {
		assertTrue(c.mult(0, 0) == 0);
	}
	
	@Test
	public void testMultWithOne() {
		assertTrue(c.mult(1, 1) == 1);
	}
	
	@Test
	public void testMultWithTwo() {
		assertTrue(c.mult(2, 2) == 4);
	}
	
	@Test
	public void testMultWithThree() {
		assertTrue(c.mult(3, 4) == 12);
	}

}
